import {getNextDaysOfTheWeek} from "../utils";


test('Getting 3 days after monday', () => {
    const actual = getNextDaysOfTheWeek("Lundi", 3);

    expect(actual).toEqual(["Lundi", "Mardi", "Mercredi"])
});

test('Getting 3 days after monday', () => {
    const actual = getNextDaysOfTheWeek("Vendredi", 5);

    expect(actual).toEqual(["Vendredi", "Samedi", "Dimanche","Lundi", "Mardi"])
});

test('Getting 3 days after monday', () => {
    const actual = getNextDaysOfTheWeek("Samedi", 5);

    expect(actual).toEqual(["Samedi", "Dimanche","Lundi", "Mardi", "Mercredi"])
});

test('Getting 3 days after monday', () => {
    const actual = getNextDaysOfTheWeek("Dimanche", 5);

    expect(actual).toEqual(["Dimanche","Lundi", "Mardi", "Mercredi", "Jeudi"])
});




